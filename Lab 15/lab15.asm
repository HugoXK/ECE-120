; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

;Name: Xu Ke
;Date: 20 May 2020
;Acknowledgement: This program is written by Xu Ke for his ECE 120 lab15.
;Purpose:This program can print out the character stored in memory from address x5002 to NULL using the character stored in address x5000 and x5001.More specificly, the character in address x5000 will be printed for every bit set to 0 in the font data, while the character in address x5001 will be printed for every bit set to 1 in the font data.
;Approach:In order to achieve its functionality, generally, this program is very likely to the previous lab14, using to counter, one for column and another for row. However, this time what we nned to print is string instead of character. Thus I add a judgement about the ASCII of each character. Each time, NULL will lead to a new line. And for each line, using x8000 to mask its most significant bit and left shift the data stored in register R5 for the next scanning. Then, for each scanned character, use BR instruction to judge and lead to corresponding operation to display (two condition, character in address x5000 for 0 and character in address x5001 for 1.) And finally, when R3 goes to 16 (ADD R3,R3,#-16; BRz) means the print process finishes and this program stops.

;Register table
;R0|For temporary use: store the mask number; store the character for OUT ; store the address of FONT_DATA.
;R1|Use as a pointer indicating which character from memory need to be loaded.
;R2|Store the ASCII loaded and calculate which line from FONT_DATA should be judged.
;R3|Use as a pointer and counter of column.
;R4|Use as a counter of row.
;R5|Load the current line data from FONT_DATA and Left shift to scan the next character in the same line.

;Program begins here

	.ORIG	x3000		;This program begins at memory location x3000.
	AND	R3,R3,#0	;Clear the previous value stored in R3 (Initialize R3 to 0).
	ADD	R4,R3,#0	;Clear the previous value stored in R4.
	ADD	R4,R4,#8	;Innitialize R4 to 8 (represent the row for each character).

RESET
	LD 	R1,CHARCTER_POINTER	;Load x5002 into R1 which indicates where the first character is.
	ADD	R1,R1,#-1		;Minus 1 to cancel the Plus effect in the next instrction for the first loop (first time it only need to add #0).
NEXT_CHARACTER
	ADD	R1,R1,#1		;Increment R1, go to next character.
	LDR	R2,R1,#0		;Load the ASCII value of character.
	BRz	ENTER			;If its x0000 (NULL), go to ENTER microgram.
	ADD	R2,R2,R2		;Double the value in R2. (2 times so far)
	ADD	R2,R2,R2		;Double the value in R2. (4 times so far)
	ADD	R2,R2,R2		;Double the value in R2. (8 times so far)
	ADD	R2,R2,R2		;Double the value in R2. (16 times so far)
	LEA	R0,FONT_DATA		;Load the address of label FONT_DATA into R0.
	ADD	R2,R2,R0		;Calculate the first line number corresponding to the character needed to be printed.
	ADD	R2,R2,R3		;Calculate the serial line number corresponding to the character needed to be printed.
	LDR	R5,R2,#0		;Load the corresponding data from FONT_DATA.
CONTINUE
	LD	R0,MASK			;Load x8000 into R0 for mask.
	AND	R0,R5,R0		;Mask the most significant bit (scan).
	BRz	JUDGEZERO		;If this bit contains 0.
	BRn 	JUDGEONE		;If this bit contains 1.(However, this is the most significant bit of 2's complement, thus negative)
SHIFT	ADD	R5,R5,R5		;Left shift (thus I can use the same value to mask the most significant bit for convenience)	
	ADD	R4,R4,#-1		;Decrement R4 (for 1-bit has been judged).
	BRp	CONTINUE		;If positive, means 8-bit judgement hasn't finished, thus continuing.
	ADD	R4,R4,#8		;Reset R4.(However after last loop, R4=0,thus no need to clear)
	BRnzp	NEXT_CHARACTER		;Go to next Character (and do the same operation).

STOP	HALT				;Stop the program.

ENTER	LD	R0,SPACE		;If NULL,store space (x0A) into R0 for print.
	OUT				;Display it.
	ADD	R3,R3,#1		;Increment R3, go to next line.
	ADD	R0,R3,#-16		;Judge whether all 16 lines have been printed.
	BRz 	STOP			;If zero, means all 16 lines have been printed, go to STOP.
	BRnzp	RESET			;Unconditional.Go back to RESET for the next line.
JUDGEZERO
	LDI	R0,ZERO			;If 0, should print a character stored in the address represented by Label ZERO.
	OUT				;Display it.
	BRnzp	SHIFT			;Unconditional.Go back to SHIFT for scan,judge and print for the next bit (in same line for the character).
JUDGEONE
	LDI	R0,ONE			;If 1, should print a character stored in the address represented by Label ONE.
	OUT				;Display it.
	BRnzp	SHIFT			;Unconditional.Go back to SHIFT for scan,judge and print for the next bit (in same line for the character)
CHARCTER_POINTER
	.FILL  	x5002			;Label CHARCTER_POINTER represents x5002 for convenience.
SPACE	.FILL	x000A			;Label SPACE represents x000A for convenience.
MASK	.FILL	x8000			;Label MASK represents x8000 for convenience.
ZERO	.FILL	x5000			;Label ZERO represents x5000 for convenience
ONE	.FILL	x5001			;Label ONE represents x5001 for convenience.


