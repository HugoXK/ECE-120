;Purpose: This program can print out the character stored in address x5002 using the character stored in address x5000 and x5001.The character in address x5000 will be printed for every bit set to 0 in the font data, while the character in address x5001 will be printed for every bit set to 1 in the font data.
;Approach: In order to achieve its functionality, generally, I use two loop to scan the data in relevant part of FONT_DATA, namely, the ROWLOOP and the COLUMNLOOP. And for each line, using x8000 to mask its most significant bit and left shift the data stored in register R5 for the next scanning. Then, for each scanned character, use BR instruction to judge and lead to corresponding operation to display (two condition, character in address x5000 for 0 and character in address x5001 for 1.)

;Register table
;R0|Temporarily store the characters that need print for the OUT operation
;R1|Use as a counter of row
;R2|use as a counter of column
;R3|Store x8000 for masking the most significant bit
;R4|Store the current scanned character (by AND with MASK)
;R5|Load the current line data from FONT_DATA and Left shift to scan the next character in the same line
;R6|Store the line number of current line.


;Initialize
	.ORIG   x3000		;This program begins at memory location x3000.
	AND	R1,R1,#0	;Clear the previous value stored in R1.
	AND	R3,R3,#0	;Clear the previous value stored in R3.
	LD	R3,MASK		;Load x8000 into R3 for mask operation in executing process.
	ADD 	R1,R1,#8	;Initialize R1 to 8 (represent the row).
	ADD 	R2,R1,R1	;Initialize R2 to 16 (represent the column).
;Execute
	LDI     R6,CHARACTER	;Load the ASCII value of the character needed to be print from memory.
	ADD     R6,R6,R6	;Double the value in R6. (2 times so far)
	ADD     R6,R6,R6	;Double the value in R6. (4 times so far)
	ADD     R6,R6,R6	;Double the value in R6. (8 times so far)
	ADD     R6,R6,R6	;Double the value in R6. (16 times so far)
	LEA     R5,FONT_DATA	;Load the address of label FONT_DATA into R5.
	ADD	R6,R6,R5	;Calculate the serial line number corresponding to the character needed to be printed.
;Column loop
COLUMNLOOP 
	BRnzp 	LOAD		;Unconditional (just for clearness of my thought, making my loop more readable)
;Row Loop

LOAD	LDR	R5,R6,#0	;Load the corresponding data from FONT_DATA.
ROWLOOP	
	AND 	R4,R5,R3	;Take the first bit in the current line.
	BRz	JUDGEZERO	;If this bit contains 0.
	BRn	JUDGEONE	;If this bit contains 1.(However, this is the most significant bit of 2's complement, thus negative)
SHIFT	ADD	R5,R5,R5	;Left shift (thus I can use the same value to mask the most significant bit for convenience)	
	ADD	R1,R1,#-1	;Decrement R1.
	BRp	ROWLOOP		;If postive, means there still have haven't been scanned bit(s) in this line, thus continuing.
	LD	R0,ENTER	;Load the ASCII value of space into R0.
	OUT			;Display it.
	ADD	R1,R1,#8	;Reset R1.(However after last loop, R1=0,thus no need to clear)
	ADD	R6,R6,#1	;Increment R6.(Go to next line)
	ADD	R2,R2,#-1	;Decrement R2.
	BRp 	COLUMNLOOP	;If positive, means there still have haven't been line(s), thus continuing.
	HALT			;Stop the program.
	
	
JUDGEZERO	
	LDI 	R0,ZERO		;If 0, should print a character stored in the address represented by Label ZERO.
	OUT			;Display it.
	BRnzp 	SHIFT		;Go back to SHIFT (for next scanning).
JUDGEONE
	LDI	R0,ONE		;If 1, should print a character stored in the address represented by Label ONE.
	OUT			;Display it.
	BRnzp	SHIFT		;Go back to SHIFT (for next scanning).
ENTER	.FILL	x000A		;Label ENTER represents x000A for convience.
ZERO	.FILL	x5000		;Label ZERO represents x5000 for convenience.
ONE	.FILL	x5001		;Label ONE represents x5001 for convenience.
CHARACTER
	.FILL 	x5002		;Label CHARACTER represents x5002 for convenience.
